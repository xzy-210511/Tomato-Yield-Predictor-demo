CREATE TABLE prediction_records (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    avg_temperature_c DOUBLE PRECISION NOT NULL,
    min_temperature_c DOUBLE PRECISION NOT NULL,
    max_temperature_c DOUBLE PRECISION NOT NULL,
    humidity_percent DOUBLE PRECISION NOT NULL,
    co2_ppm DOUBLE PRECISION NOT NULL,
    light_intensity_lux DOUBLE PRECISION NOT NULL,
    photoperiod_hours DOUBLE PRECISION NOT NULL,
    irrigation_mm DOUBLE PRECISION NOT NULL,
    fertilizer_n_kg_ha DOUBLE PRECISION NOT NULL,
    fertilizer_p_kg_ha DOUBLE PRECISION NOT NULL,
    fertilizer_k_kg_ha DOUBLE PRECISION NOT NULL,
    pest_severity DOUBLE PRECISION NOT NULL,
    ph DOUBLE PRECISION NOT NULL,
    variety VARCHAR(50) NOT NULL,
    predicted_yield_kg_per_m2 DOUBLE PRECISION NOT NULL,
    model_version VARCHAR(50),
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX idx_prediction_records_created_at
    ON prediction_records (created_at);

CREATE INDEX idx_prediction_records_variety
    ON prediction_records (variety);
